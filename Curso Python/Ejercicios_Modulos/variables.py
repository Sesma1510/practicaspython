# Aqui hay una variable de tipo entero
numero = 147
print(numero) # Esta linea sirve para imprimir la variable numero
print(type(numero))

# Redefinimos la variable numero
numero = 147.5
print(numero)
print(type(numero))

# Aqui hay una variable de tipo complejo
complejo = 1 + 2j
print(complejo)
print(type(complejo))

# Aqui hay una variable de tipo string
palabra = 'Hola'
print(palabra)
print(type(palabra))

# Aqui hay una variable de tipo bool
bandera = True
print(type(bandera))

# Aqui hay una variable de tipo Null
vacio = None
print(vacio)
print(type(vacio))